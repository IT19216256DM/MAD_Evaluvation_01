package com.example.orderus;

public class Employee {

    private String txtId;
    private String txtName;
    private String txtAdd;
    private Integer txtNum;
    private String txtMail;
    private Integer txtOwn;

    public Employee() {
    }

    public String getTxtId() {
        return txtId;
    }

    public void setTxtId(String txtId) {
        this.txtId = txtId;
    }

    public String getTxtName() {
        return txtName;
    }

    public void setTxtName(String txtName) {
        this.txtName = txtName;
    }

    public String getTxtAdd() {
        return txtAdd;
    }

    public void setTxtAdd(String txtAdd) {
        this.txtAdd = txtAdd;
    }

    public Integer getTxtNum() {
        return txtNum;
    }

    public void setTxtNum(Integer txtNum) {
        this.txtNum = txtNum;
    }

    public String getTxtMail() {
        return txtMail;
    }

    public void setTxtMail(String txtMail) {
        this.txtMail = txtMail;
    }

    public Integer getTxtOwn() {
        return txtOwn;
    }

    public void setTxtOwn(Integer txtOwn) {
        this.txtOwn = txtOwn;
    }
}